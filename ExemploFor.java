public class ExemploFor {
    public static void main(String[] args) {
        // Uso do for: repetir um número fixo de vezes
        System.out.println("Contagem regressiva:");
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("Lançamento!");
    }
}
