public class tiposprimitivos {
    public static void main(String[] args) {
        int numero = 10;
        double preco = 5.99;
        char letra = 'C';
        boolean status = true;

        System.out.println("Número: " + numero);
        System.out.println("Preço: " + preco);
        System.out.println("Letra: " + letra);
        System.out.println("Status: " + status);
    }
}
